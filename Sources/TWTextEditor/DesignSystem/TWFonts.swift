import UIKit

extension UIFont {
    static func twFontReqularWithSize(size: CGFloat) -> UIFont {
        return UIFont(name: "ArbFONTS-DINNextLTArabic-Regular-2", size: size)!
    }
    
    static func twFontMediumWithSize(size: CGFloat) -> UIFont {
        return UIFont(name: "ArbFONTS-DINNextLTArabic-Medium-2", size: size)!
    }
    
    static func twFontBoldWithSize(size: CGFloat) -> UIFont {
        return UIFont(name: "ArbFONTS-DINNextLTArabic-Bold-2", size: size)!
    }
}
